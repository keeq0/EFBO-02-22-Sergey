import React from 'react';
import "./items-list.css";

import photo from '../../images/catalog2.jpg';
import { items } from './items';

const ItemsList = () => {

    const flipCard = (id) => {
        const card = document.getElementById(id);
        card.classList.remove("resetflip");
        card.classList.add("flip");
    };

    const resetFlip = (id) => {
        const card = document.getElementById(id);
        card.classList.remove("flip");
        card.classList.add("resetflip");
    };

    return (
        <section className="itemslist">
            <ul className="itemslist__list">
                {items.map((item, index) => (
                    <li className="itemslist__list-item" key={index}>
                        <div id={`card-${index}`} className="item__inner">
                            <div className="item__front">
                                <img src={item.src} alt="Фото еды" className="item__photo" />
                                <div className="item__info">
                                    <h3 className="item__title">{item.title}</h3>
                                    <p className="item__description">{item.description}</p>
                                    <p className="item__price">{item.price + " ₽"}</p>
                                    <div className="item__buttons">
                                        <button className="item__tocart">В корзину</button>
                                        <button className="item__about" onClick={() => flipCard(`card-${index}`)}>Подробнее</button>
                                    </div>
                                </div>
                            </div>
                            <div className="item__back">
                                <div className="item__info">
                                    <h3 className='item__title'>{item.title}</h3>
                                    <p className="item__description">{item.description}</p>
                                    <h3 className='item__title'>Ингредиенты:</h3>
                                    <p className='item__ingredients'>{item.ingredients}</p>
                                    <h3 className='item__title'>Время приготовления:</h3>
                                    <p className='item__ingredients'>{item.cook_time}</p>
                                    <h3 className='item__title'>Энерг. ценность:</h3>
                                    <p className='item__ingredients'>{item.energy}</p>
                                    <p className="item__price">{item.price + " ₽"}</p>
                                    <button className="item__btn-back" onClick={() => resetFlip(`card-${index}`)}>Закрыть</button>
                                </div>
                            </div>
                        </div>
                    </li>
                ))}
            </ul>
            <img src={photo} className="itemslist__photo" alt="Фото еды" />
        </section>
    );
}

export default ItemsList;
