import './category-list.css';

import { categories } from './categories';

import { useState } from 'react';

const CategoryList = () => {

    const [activeCategory, setActiveCategory] = useState('БЛЮДА');

    const handleCategoryClick = (category) => {
        setActiveCategory(category);
    };


    return (
        <section className='categorylist'>
            <ul className='categorylist__list'>
            {categories.map((category) => (
                <li
                    className={`categorylist__list-item ${category.name === activeCategory ? 'category-active' : ''}`}
                    onClick={() => handleCategoryClick(category.name)}
                >
                    <p className='categorylist__list-category'>
                        {category.name}
                    </p>
                </li>
            ))}
            </ul>
        </section>
    );
}

export default CategoryList;